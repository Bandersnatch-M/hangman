# Chapter 5. Practice 2-6. Lists. Метод pop - удаление последнего элемента.


fruit = ["grape", "plum", "banana"]
print(fruit)

"""С помощью метода "pop" можно удалить последний элемент в списке."""
# Важно!!! Индексы имеют нумерацию с "0", т.е. 0, 1, 2 и т.д.!!!"""

item = fruit.pop()
print(item)
print(fruit)

