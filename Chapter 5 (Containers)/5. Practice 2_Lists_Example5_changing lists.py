# Chapter 5. Practice 2-5. Lists. Изменение списка.


fruit = ["grape", "plum", "banana"]
print(fruit)

"""Списки изменяемы . Когда контейнер является изменяемым, это значит, что
вы можете добавлять в него объекты или удалять их. Изменить объект в списке
можно, присвоив его индекс новому объекту."""
# Важно!!! Индексы имеют нумерацию с "0", т.е. 0, 1, 2 и т.д.!!!"""
"""Меняем banana на apple"""

fruit[2] = "apple"
print(fruit)

