# Chapter 5. Practice 2-2. Lists. Methods: "Append"


fruit = ["grape", "plum", "banana"]

"""В нашем списке три элемента: "grape", "plum" и "banana". Спи-
ски хранят элементы в определенном порядке.
Если не менять порядок списка, "grape" всегда будет первым элементом, "plum"
— вторым и "banana" — третьим. "grape" представляет собой начало списка, а
"banana" — конец. Добавление в список нового элемента осуществляется с по-
мощью метода append. Пример ниже"""

fruit.append("apple")
fruit.append("orange")
print(fruit)
