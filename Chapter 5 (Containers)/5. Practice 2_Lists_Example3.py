# Chapter 5. Practice 2-2. Lists. Methods: "Append"


fruit = list("melon")

"""Метод append работет и для функции list(), если мы хотим добавить объекты целиком не разбивая на символы. Добавление в список нового элемента осуществляется с по-
мощью метода append. Пример ниже"""

fruit.append("apple")
fruit.append("orange")
print(fruit)

"""Пример второй"""


