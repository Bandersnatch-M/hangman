# Programm 6 (Different strings depending on whole number for "age")

age = 65
if age <= 2:
    print("Infant")
elif age <= 6:
    print("Child")
elif age <= 12:
    print("Pre-teen")
elif age <= 18:
    print("Teenager")
elif age <= 24:
    print("Young adult")
elif age <= 49:
    print("Adult")
elif age <= 64:
    print("Senior citizen")
else:
    print("Elderly man")
